namespace app\common\traits;

use think\exception\HttpResponseException;
use think\Response;

trait Apis
{

    /**
     * 操作成功返回的数据
     * @param string $msg 提示信息
     * @param mixed $data 要返回的数据
     * @param int $code 错误码，默认为1
     * @param string $type 输出类型
     * @param array $header 发送的 Header 信息
     */
    public  function success($msg = '', $data = '', $code = 200, $type = 'json', array $header = [], $options= [])
    {
        $this->result($msg, $data, $code, $type, $header,$options);
    }

    /**
     * 操作失败返回的数据
     * @param string $msg 提示信息
     * @param mixed $data 要返回的数据
     * @param int $code 错误码，默认为0
     * @param string $type 输出类型
     * @param array $header 发送的 Header 信息
     */
    public function error($msg = '', $data = '', $code = 404, $type = 'json', $header = [], $options=[])
    {
        $this->result($msg, $data, $code, $type, $header,$options);
    }


    /**
     * 操作成功返回的数据
     * @param string $msg 提示信息
     * @param mixed $data 要返回的数据
     * @param int $code 错误码，默认为1
     * @param array $header 发送的 Header 信息
     */
    public  function json($msg = '', $data = '', $code = 200, array $header = [], $options= [])
    {
        $result = [
            'code' => $code,
            'msg' => $msg,
            'time' => \think\facade\Request::instance()->server('REQUEST_TIME'),
            'data' => $data,
        ];
        // 如果未设置类型则自动判断
        // 发送头部信息
        foreach ($header as $name => $val) {
            if (is_null($val)) {
                header($name);
            } else {
                header($name . ':' . $val);
            }
        }
        return json($result,$code,$header,$options);
    }
    protected  function result($msg, $data = '', $code = 404, $type = 'json',  $header = [], $options = [])
    {
        $result = [
            'code' => $code,
            'msg' => $msg,
            'time' => \think\facade\Request::instance()->server('REQUEST_TIME'),
            'data' => $data,
        ];
        // 如果未设置类型则自动判断
        // 发送头部信息
        foreach ($header as $name => $val) {
            if (is_null($val)) {
                header($name);
            } else {
                header($name . ':' . $val);
            }
        }
        $response =  Response::create($result, $type)->header($header)->options($options);
        throw new HttpResponseException($response);
    }



}

